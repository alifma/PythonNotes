
# Operasi Operasi aritmatika
a = 10
b = 3

# Add (+)
hasil = a + b
print("Hasil tambah: ", hasil)

# Subtract (-)
hasil = a - b
print("Hasil kurang: ", hasil)

# Divide (/)
hasil = a / b
print("Hasil bagi: ", hasil)

# Multiply/Kali (*)
hasil = a * b
print("Hasil kali: ", hasil)

# Pangkat (**)
hasil = a ** b
print("Hasil pangkat: ", hasil)

# Modulus (%)
hasil = a % b
print("Hasil modulo: ", hasil)

# Floor Div (//) kebalikan modulo
hasil = a // b
print("Hasil floor div: ", hasil)

# Prioritas operasi
# - Kurung ( )
# - Eksponen (pangkat / akar)
# - Perkalian / Pembagian
# - Penjumlahan, Perkalian

x = 2
y = 3
z = 4
hasil =  x ** y / z + x - y % x // y
print("Hasil prioritas: ", hasil)